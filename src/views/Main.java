    private void animalsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_animalsTableMouseClicked
        // TODO add your handling code here:
        int column = animalsTable.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/animalsTable.getRowHeight();
        
        if(row < animalsTable.getRowCount() && row >=0 && column < animalsTable.getColumnCount() && column >= 0){
            Object value = animalsTable.getValueAt(row, column);
            if(value instanceof JButton){
                try {
                    ((JButton) value).doClick();
                    JButton button = (JButton) value;
                    String code = tableModel.getValueAt(row, 0).toString();
                    Taxonomy taxonomy = controller.manageTaxonomy(code, "*");
                    ManageAnimal ma = new ManageAnimal(taxonomy);
                    ma.setVisible(true);
                    this.dispose();
                } catch (SQLException ex) {
                }
            }
        }
    }//GEN-LAST:event_animalsTableMouseClicked

    private void updateTable(){
        try {
            for (int i = 0; i < 3; i++) {
                for (int j = 0; j < tableModel.getRowCount(); j++) {
                    tableModel.removeRow(j);
                }
            }
        } catch (Exception e) {
        }try{
            ArrayList<Object[]> rows = animals;
            for (int i = 0; i < rows.size(); i++) {
                rows.get(i)[2] = btnEditProduct;
                tableModel.addRow(rows.get(i));
            }
        }catch (Exception e){   
            e.printStackTrace();
        }
    }
    
